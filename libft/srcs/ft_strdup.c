#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	char	*res;

	if (s == NULL)
		return (NULL);
	res = (char*)malloc(sizeof(*s) * (ft_strlen(s) + 1));
	if (res == NULL)
		return (NULL);
	i = 0;
	while (s[i])
	{
		res[i] = s[i];
		i++;
	}
	res[i] = '\0';
	return (res);
}
